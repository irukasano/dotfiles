#!/usr/bin/env bash
set -euo pipefail

usage() {
  cat <<'EOF'
Usage:
  gh-pr-create.sh [gh pr create options except --fill/--body/--body-file] --title "PR title"

Example:
  gh-pr-create.sh -B develop --reviewer alice --assignee bob --title "変更履歴 基本設計"

Optional env vars:
  PR_BODY_DEBUG=1  Keep generated body file for inspection
EOF
}

die() {
  printf 'Error: %s\n' "$*" >&2
  exit 1
}

require_cmd() {
  command -v "$1" >/dev/null 2>&1 || die "required command not found: $1"
}

summary_prompt="$(cat <<'EOF'
以下の git diff の内容と以下の「サマリの書き方」をもとにサマリを平易な日本語で書きます

サマリの書き方：
* 日本語で書きます
* 箇条書き 1, 2, 3, ... で要点を整理します
* 変更内容が少ない場合は箇条書き一つでまとめます
* 全体を200文字以内にまとめます

--- git diff ---
EOF
)"

require_cmd git
require_cmd codex
require_cmd gh
require_cmd perl

base_branch=""
title=""
declare -a passthrough_args=()

while (($# > 0)); do
  case "$1" in
    -B|--base)
      (($# >= 2)) || die "$1 requires a value"
      base_branch="$2"
      passthrough_args+=("$1" "$2")
      shift 2
      ;;
    --title|-t)
      (($# >= 2)) || die "$1 requires a value"
      title="$2"
      shift 2
      ;;
    --fill|--fill-first|--fill-verbose)
      # ignore autofill-related flags; body is generated by this wrapper
      shift
      ;;
    --body|-b|--body-file|-F)
      die "do not pass $1; this wrapper generates PR body automatically"
      ;;
    --help|-h)
      usage
      exit 0
      ;;
    *)
      passthrough_args+=("$1")
      shift
      ;;
  esac
done

[[ -n "$title" ]] || die "--title is required"

git rev-parse --is-inside-work-tree >/dev/null 2>&1 || die "not inside a git repository"

current_branch="$(git branch --show-current)"
[[ -n "$current_branch" ]] || die "failed to detect current branch"

if [[ -z "$base_branch" ]]; then
  # gh pr create defaults base branch from repo settings if omitted.
  # For body generation we need a concrete comparison target.
  default_branch="$(gh repo view --json defaultBranchRef --jq '.defaultBranchRef.name' 2>/dev/null || true)"
  [[ -n "$default_branch" ]] || die "base branch not specified and failed to detect repository default branch"
  base_branch="$default_branch"
fi

git fetch origin "$base_branch" >/dev/null 2>&1 || true

merge_base="$(git merge-base "origin/$base_branch" HEAD 2>/dev/null || true)"
[[ -n "$merge_base" ]] || die "failed to compute merge-base against origin/$base_branch"

commit_count="$(git rev-list --count "${merge_base}..HEAD")"
[[ "$commit_count" -gt 0 ]] || die "no commits found between origin/$base_branch and HEAD"

commits_md="$(
  git log --reverse --pretty=format:'- %s (%h)' "${merge_base}..HEAD"
)"

# Issue番号抽出:
# 1. title から fixes/closes/resolves #123
# 2. commit subjects/messages から同様のパターン
extract_issue_ref() {
  perl -ne '
    while (/\b(?:close[sd]?|fix(?:e[sd])?|resolve[sd]?)\s+#([0-9]+)/ig) {
      print "$1\n";
    }
  ' | awk '!seen[$0]++'
}

issue_numbers="$(
  {
    printf '%s\n' "$title"
    git log --format='%s%n%b' "${merge_base}..HEAD"
  } | extract_issue_ref
)"

issue_block=""
if [[ -n "$issue_numbers" ]]; then
  while IFS= read -r n; do
    [[ -n "$n" ]] || continue
    issue_block+="fixes #$n"$'\n'
  done <<< "$issue_numbers"
  issue_block+=$'\n'
fi

diff_text="$(git diff --find-renames --stat=200,120 "${merge_base}..HEAD")"$'\n\n'"$(git diff --find-renames "${merge_base}..HEAD")"

run_summary_cmd() {
  local prompt_file output_last
  local -a cmd

  prompt_file="$(mktemp -t gh-pr-summary-prompt.XXXXXX.txt)"
  output_last="$(mktemp -t gh-pr-summary-output.XXXXXX.txt)"

  {
    printf '%s\n' "$summary_prompt"
    printf '%s' "$diff_text"
  } > "$prompt_file"

  cmd=(codex exec --skip-git-repo-check --output-last-message "$output_last")
  if [[ -n "${CODEX_MODEL:-}" ]]; then
    cmd+=(-m "$CODEX_MODEL")
  fi

  "${cmd[@]}" - < "$prompt_file" >/dev/null 2>&1 || true

  if [[ -s "$output_last" ]]; then
    cat "$output_last"
    rm -f "$prompt_file" "$output_last"
    return 0
  fi

  rm -f "$prompt_file" "$output_last"
  return 1
}

generate_summary() {
  if run_summary_cmd; then
    return 0
  fi

  printf '<!-- summary generation failed; please edit manually -->\n'
}

summary_md="$(generate_summary)"

body_file="$(mktemp -t gh-pr-body.XXXXXX.md)"
cleanup() {
  if [[ "${PR_BODY_DEBUG:-0}" = "1" ]]; then
    printf 'Generated body file kept at: %s\n' "$body_file" >&2
  else
    rm -f "$body_file"
  fi
}
trap cleanup EXIT

{
  printf '%s' "$issue_block"
  cat <<EOF
## Summary

$summary_md

## Changes

$commits_md

## Comment

<!-- なにを重点的にみてもらいたいか、主な修正点、先行PRなど -->

EOF
} > "$body_file"

"${EDITOR:-vi}" "$body_file"

printf '\n===== PR BODY PREVIEW =====\n\n'
cat "$body_file"
printf '\n===== END PREVIEW =====\n\n'

read -r -p "Create PR with this body? [y/N]: " answer
case "$answer" in
  [yY]|[yY][eE][sS])
    ;;
  *)
    printf 'Aborted.\n' >&2
    exit 1
    ;;
esac

gh pr create \
  "${passthrough_args[@]}" \
  --title "$title" \
  --body-file "$body_file"


